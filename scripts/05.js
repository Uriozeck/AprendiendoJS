//Tipos de datos

/*Numeros: este incluye a todos tanto reales, naturales, flotantes (decimales), al declararlos estos no usan comillas pero si se declara un nuero con comillas esto lo declara como un string*/

const numero = 100;
const numero1 = 200.20;
const numero2 = "300";

console.log(numero);
console.log(numero1);
console.log(numero2);

/**Operaciones con numeros */
console.log(numero + numero1); // Suma
console.log(numero1 - numero); // Resta
console.log(numero1 * numero); // Multiplicación
console.log(numero1 / numero); // División
console.log(numero1 % numero); // Módulo

